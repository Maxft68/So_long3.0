#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int			i;
	int			sign;
	int			result;
	const char	*s;

	i = 0;
	sign = 1;
	result = 0;
	s = nptr;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		result = result * 10 + (s[i] - '0');
		i++;
	}
	return (result * sign);
}
