#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	j;

	j = 1;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	else
	{
		if (n < 0)
		{
			j += write(1, "-", 1);
			n = n * -1;
		}
		if (n > 9)
		{
			j += ft_putnbr(n / 10);
		}
		ft_putchar(n % 10 + '0');
	}
	return (j);
}
